    private void btnAgregarAulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAulaActionPerformed
        Aula aula = new Aula();
        AulaDAO au = new AulaDAO();      
        //Recogemos los datos para su proxima INSERT
        String grado_instruccion = cb_gradoInstruccion.getSelectedItem().toString();
        int grado = Integer.parseInt(cbgrado.getSelectedItem().toString());
        String curso = cbCurso.getSelectedItem().toString();
        String cursop=cbCurso1.getSelectedItem().toString();
        int index = cbProfesores.getItemAt(cbProfesores.getSelectedIndex()).getCoddoc();//Recepcionamos el ID del docente Seleccionado
        if(grado_instruccion.equalsIgnoreCase("Primaria")){
            aula.setCurso(cursop);
            aula.setIdgrado(grado);
            aula.setIdseccion(seccion);
            aula.setIddocente(index);
            au.AgregarAula(aula);
        }
        else{
            aula.setCurso(curso);
            aula.setIdgrado(grado);
            aula.setIdseccion(seccion);
            aula.setIddocente(index);
            au.AgregarAula(aula);
        }
    }//GEN-LAST:event_btnAgregarAulaActionPerformed
    
