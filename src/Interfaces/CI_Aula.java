    private void cb_gradoInstruccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_gradoInstruccionActionPerformed
        DocenteDAO dao = new DocenteDAO();
        String gi = cb_gradoInstruccion.getSelectedItem().toString();
            cbProfesores.removeAllItems();   //eliminar el contenido del combo box   
        if (gi.equalsIgnoreCase("Primaria")) {
            cbCurso.setVisible(false);
            cbCurso1.setVisible(true);
            lbcs.setVisible(false);
            lbcp.setVisible(true);
            ginst = 1;
            dao.ListarDocentes(cbProfesores, ginst);
            
        }
        if (gi.equalsIgnoreCase("Secundaria")) {
            cbCurso.setVisible(true);
            cbCurso1.setVisible(false);
            lbcs.setVisible(true);
            lbcp.setVisible(false);
            ginst = 2;
            dao.ListarDocentes(cbProfesores, ginst);
            
        }
    }//GEN-LAST:event_cb_gradoInstruccionActionPerformed

